    private void btnBorrowActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBorrowActionPerformed
        // TODO add your handling code here:
        SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd");
        try {
            Date returnDate = sdf.parse(txtEx.getText().trim());
            BookDAO bookdao = new BookDaoImp();
            BorrowDAO borrowDao = new BorrowImp();

            BookPurchase bp = bookdao.getBookbyId(Integer.parseInt(txtBn.getText()));
            BookIssue bookissue = new BookIssue(txtN.getText().trim(), txtC.getText().trim(), txtIsbn.getText().trim(), txtBT.getText().trim(), bp, Integer.parseInt(txtQ.getText().trim()), Integer.parseInt(txtAv.getText().trim()), new Date(), returnDate);
            borrowDao.save(bookissue);

           JOptionPane.showMessageDialog(null, "Issue success");
            SummaryDao smd = new SummaryDaoImp();
            try {
                BookSummary sm = smd.getSummarybyISBN(txtBT.getText().trim());
                if (sm != null) {
                    int totalqty = sm.getTotalQty();
                    int issueQty = sm.getIssueQty() + Integer.parseInt(txtQ.getText().trim());
                    int availableqty = sm.getAvilableQty() - Integer.parseInt(txtQ.getText().trim());
                    BookSummary sumUp = new BookSummary(sm.getIsbn(), totalqty, issueQty, availableqty);
                    smd.update(sumUp);
JOptionPane.showMessageDialog(null, "issue success");

                }
            } catch (Exception e) {
                e.printStackTrace();
            } 
        } catch (ParseException ex) {
            Logger.getLogger(IssueOfBooks.class.getName()).log(Level.SEVERE, null, ex);
        }
        displayBooklistIntoTables();


    }//GEN-LAST:event_btnBorrowActionPerformed

