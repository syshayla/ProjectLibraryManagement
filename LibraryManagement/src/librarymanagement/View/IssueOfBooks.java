    public void displayBooklistIntoTables() {
        clearTable();
        BorrowDAO dao = new BorrowImp();
        List<BookIssue> list = dao.getBorrow();
        DefaultTableModel model = (DefaultTableModel) tblDisplay.getModel();
        Object[] cols = new Object[10];
        for (int i = 0; i < list.size(); i++) {
            cols[0] = list.get(i).getId();
            cols[1] = list.get(i).getReaderName();
            cols[2] = list.get(i).getContact();
            cols[3] = list.get(i).getISBN();

            cols[4] = list.get(i).getBookTitle();

            cols[5] = list.get(i).getBookid();
            cols[6] = list.get(i).getIssueQty();
            cols[7] = list.get(i).getAvailQty();

            cols[8] = list.get(i).getDateOfIssue();
            cols[9] = list.get(i).getDateOfExpiry();
            model.addRow(cols);

        }
    }

